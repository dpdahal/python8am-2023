# what is operators?
# Operators are used to perform operations on variables and values.
# Python divides the operators in the following groups:
# Arithmetic operators: +, -, *, /, %, **, //
# Assignment operators: =, +=, -=, *=, /=, %=, **=, //=
# Comparison operators: ==, !=, >, <, >=, <=
# Logical operators: and, or, not
# Identity operators: is, is not
# Membership operators: in, not in
# Bitwise operators: &, |, ^, ~, <<, >>
#
# Arithmetic operators
# +	Addition	x + y
# -	Subtraction	x - y
# *	Multiplication	x * y
# /	Division	x / y
# %	Modulus	x % y
# **	Exponentiation	x ** y
# //	Floor division	x // y
#
# print(10%3)
# print(4**2)
# print(19//5)
#
# Assignment operators
# =	x = 5	x = 5
#
# x = 5
# x = x + 3
# print(x)
#
# result =(5*5) + 10 * 2
# print(result)
#
# Comparison operators
# ==	Equal	x == y
# !=	Not equal	x != y
# >	Greater than	x > y
# <	Less than	x < y
# >=	Greater than or equal to	x >= y
# <=	Less than or equal to	x <= y
#
# print(5 == 5)
# print(5 != 5)
# print(5 <7)
#
# Logical operators
# and 	Returns True if both statements are true	x < 5 and  x < 10
# or	Returns True if one of the statements is true	x < 5 or x < 4
# not	Reverse the result, returns False if the result is true	not(x < 5 and x < 10)
#
# print(5 < 10 and 5 < 20)
# print(5 < 10 or 5 < 4)
# print(not(5 < 10 and 5 < 20))
#
# Identity operators
# is 	Returns True if both variables are the same object	x is y
# is not	Returns True if both variables are not the same object	x is not y
#
# x = 5
# y = 10
# z = x
# print(x is y)
# print(x is z)
# print(x is not y)
# print(x is not z)
#
# Membership operators
# in 	Returns True if a sequence with the specified
# value is present in the object	x in y
# not in	Returns True if a sequence with the
# specified value is not present in the object	x not in y
#
#
# x = ["apple", "banana"]
# print("banana" in x)
# print("pineapple" not in x)
#
# Bitwise operators
# & 	AND	Sets each bit to 1 if both bits are 1
# |	OR	Sets each bit to 1 if one of two bits is 1
# ^	XOR	Sets each bit to 1 if only one of two bits is 1
# ~ 	NOT	Inverts all the bits
# <<	Zero fill left shift	Shift left by pushing zeros in
# from the right and let the leftmost bits fall off
# >>	Signed right shift	Shift right by pushing copies
# of the leftmost bit in from the left, and let the rightmost bits fall off
#
# print(5 & 6)
# print(bin(5))
# print(bin(6))
# print(bin(4))
#
